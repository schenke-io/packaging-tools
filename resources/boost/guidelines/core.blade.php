## schenke-io/packaging-tools

Tools to simplify publishing github packages

### Features

- Dynamic SVG Badges: Generate SVG badges for project metrics
- AI-Ready Boost Guidelines: Write AI guidelines and skills for projects based on Laravel Boost standards
- Elegant Migration Management: Clean up and manage database migrations
- Modular Markdown Assembler: Assemble modular documentation and class references
- Swift Project Setup: Basic installation and configuration
- Turbo Speed Seeding: Speeds up database preparation in tests by loading a pre-generated SQL dump
