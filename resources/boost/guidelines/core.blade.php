## schenke-io/packaging-tools

Tools to simplify publishing github packages

### Features

- packaging_tools_badges: Generate SVG badges for project metrics
- packaging_tools_guidelines: Write AI guidelines and skills for projects based on Laravel Boost standards
- packaging_tools_imported_migrations: Clean up and manage database migrations
- packaging_tools_markdown_assembly: Assemble modular documentation and class references
- packaging_tools_setup: Basic installation and configuration
- packaging_tools_speed_seeding: Speeds up database preparation in tests by loading a pre-generated SQL dump
